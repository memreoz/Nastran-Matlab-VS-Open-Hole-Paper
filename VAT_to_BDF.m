%% =========================================================================
%  VAT_to_BDF.m
%  Lam_Design + run_patch_modifier birlesimi
% =========================================================================
clc; clear; close all;

%% ?? YOLLAR ???????????????????????????????????????????????????????????????
BASE_DIR    = 'D:\Document\VSCL\ANALYSIS\NASTRAN_MATLAB_VS_PRJ';
BDF_IN      = fullfile(BASE_DIR, 'OHC_VS_PATCH_SET_MATLAB.bdf');
NASTRAN_EXE = 'D:\MSC Nastran\bin\nastranw.exe';

%% ?? PANEL & MESH ?????????????????????????????????????????????????????????
a = 20; b = 20;
nex = 10; ney = 10;

%% ?? T0 / T1 ??????????????????????????????????????????????????????????????
T0 = 30;   % Merkez acisi [deg]
T1 = -50;   % Kenar acisi  [deg]

BDF_OUT = fullfile(BASE_DIR, sprintf('OHC_VAT_T0_%d_T1_%d.bdf', T0, T1));

%% ?? KONTROL NOKTALARI ????????????????????????????????????????????????????
ctrl = [
    0,  0,  T1;
    0,  20, T1;
    20, 0,  T1;
    20, 20, T1;
    0,  10, T1;
    20, 10, T1;
    10, 0,  T1;
    10, 20, T1;
    10, 10, T0;
];
xp = ctrl(:,1); yp = ctrl(:,2); thp = ctrl(:,3);

%% ?? FE MESH ACILARI ??????????????????????????????????????????????????????
xn = linspace(0,a,nex+1);
yn = linspace(0,b,ney+1);
xe = (xn(1:end-1)+xn(2:end))/2;
ye = (yn(1:end-1)+yn(2:end))/2;
[Xc,Yc] = meshgrid(xe,ye);
Theta_e  = griddata(xp,yp,thp,Xc,Yc,'v4');

%% ?? ELEM_FIBER MATRISI ???????????????????????????????????????????????????
elem_fiber = zeros(nex*ney, 2);
for row = 1:ney
    for col = 1:nex
        eid = (row-1)*nex + col;
        elem_fiber(eid,1) = eid;
        elem_fiber(eid,2) = Theta_e(row,col);
    end
end

%% ?? EC STRUCT'A DONUSTUR ?????????????????????????????????????????????????
ec = struct('elem_id', {}, 'angles', {});
for i = 1:size(elem_fiber,1)
    theta = round(elem_fiber(i,2), 4);
    % 0 gelirse cok kucuk bir deger ver
    if abs(theta) < 1e-6
        theta = 0.0001;
    end
    ec(end+1).elem_id = elem_fiber(i,1);
    ec(end).angles    = [theta, -theta, theta];
end

%% ?? OPSIYONLAR ???????????????????????????????????????????????????????????
opts.bdf_output = BDF_OUT;
opts.mat_id     = 1;
opts.ply_t      = 0.131;
opts.fail_flag  = 'YES';
opts.verbose    = true;
opts.verify     = true;

%% ?? CALISTIR ?????????????????????????????????????????????????????????????
result = modify_patch_bdf(BDF_IN, ec, opts);

fprintf('Cikti BDF  : %s\n', result.bdf_output);
fprintf('Dogrulama  : %s\n', ternary(result.verify_ok, 'GECTI', 'HATA VAR'));
fprintf('Satir delta: %+d\n', result.n_lines_out - result.n_lines_in);

%% ?? NASTRAN CALISTIR ?????????????????????????????????????????????????????
if result.verify_ok
    % Calisma dizinini BDF klasoru yap
    cd(BASE_DIR);
    
    f06_file = run_nastran(NASTRAN_EXE, BDF_OUT);
    fprintf('F06 dosyasi: %s\n', f06_file);

    buck = read_f06_buckling(f06_file);
    lambda_cr = buck.lambda(1);
    fprintf('T0=%d T1=%d -> Lambda_cr = %.4f\n', T0, T1, lambda_cr);
else
    fprintf('BDF dogrulama hatasi, Nastran calistirilmadi.\n');
end

%% ?? YEREL YARDIMCI ???????????????????????????????????????????????????????
function s = ternary(cond, a, b)
    if cond, s = a; else, s = b; end
end
%% VAT 2D - T0/T1 Parametrik Laminasyon Tan?m?
% T0: merkez aç?s? (x=a/2, y=b/2)
% T1: kenar aç?lar? (tüm kö?eler ve kenar orta noktalar?)
% Notasyon: <T1 | T0 | T1>

clear; clc; close all;

%% ?? Panel & Mesh ?????????????????????????????????????????????????????????
a   = 20;   b   = 20;
nex = 10;    ney = 10;

%% ?? T0 / T1 Parametreleri ????????????????????????????????????????????????
T0 = 45;    % Merkez aç?s? [°]
T1 = 0;     % Kenar aç?s?  [°]

%% ?? Kontrol noktalar? (T0/T1'den otomatik üret) ?????????????????????????
ctrl = [
    % Kö?eler ? T1
    0.0, 0.0, T1;
    0.0, 20, T1;
    20, 0.0, T1;
    20, 20, T1;
    % Kenar orta noktalar? ? T1
    0.0, 10, T1;
    20, 10, T1;
    10, 0.0, T1;
    10, 20, T1;
    % Merkez ? T0
    10, 10, T0;
];
xp = ctrl(:,1); yp = ctrl(:,2); thp = ctrl(:,3);

%% ?? FE Mesh merkez koordinatlar? & aç?lar ????????????????????????????????
xn = linspace(0,a,nex+1);
yn = linspace(0,b,ney+1);
xe = (xn(1:end-1)+xn(2:end))/2;
ye = (yn(1:end-1)+yn(2:end))/2;
[Xc,Yc] = meshgrid(xe,ye);
Theta_e  = griddata(xp,yp,thp,Xc,Yc,'v4');

%% ?? Sürekli aç? alan? (arka plan) ???????????????????????????????????????
Nf = 300;
[Xf,Yf] = meshgrid(linspace(0,a,Nf), linspace(0,b,Nf));
Theta_f  = griddata(xp,yp,thp,Xf,Yf,'v4');

%% ?? Görselle?tirme ???????????????????????????????????????????????????????
figure('Name','VAT T0/T1','Position',[80 80 900 800]);
hold on;

% 1) Arka plan
contourf(Xf,Yf,Theta_f,30,'LineColor','none');
colormap(jet); caxis([min(T0,T1) max(T0,T1)]);
cb = colorbar; cb.Label.String = '\theta [°]';

% 2) FE mesh
for i = 1:nex+1
    plot([xn(i) xn(i)],[0 b],'k-','LineWidth',0.4);
end
for j = 1:ney+1
    plot([0 a],[yn(j) yn(j)],'k-','LineWidth',0.4);
end

% 3) Merkez oklar
sc = min(a/nex,b/ney)*0.42;
U  =  cosd(Theta_e)*sc;
V  =  sind(Theta_e)*sc;
quiver(Xc,Yc, U, V,0,'k','LineWidth',1.2,'MaxHeadSize',0.6);
quiver(Xc,Yc,-U,-V,0,'k','LineWidth',1.2,'MaxHeadSize',0.6);
plot(Xc(:),Yc(:),'k.','MarkerSize',4);

% 4) Kontrol noktalar?
scatter(xp,yp,80,'w','s','filled','MarkerEdgeColor','k','LineWidth',1.5);
for k = 1:length(xp)
    text(xp(k)+0.02,yp(k)+0.02,sprintf('%.0f°',thp(k)),...
        'Color','w','FontSize',9,'FontWeight','bold');
end

rectangle('Position',[0 0 a b],'EdgeColor','k','LineWidth',2);
xlim([0 a]); ylim([0 b]); axis equal tight;
xlabel('x [m]'); ylabel('y [m]');
title(sprintf('VAT 2D  –  \\langle T_1=%d° | T_0=%d° | T_1=%d° \\rangle', ...
    T1, T0, T1), 'FontSize',13);

fprintf('Laminasyon: <T1=%d° | T0=%d° | T1=%d°>\n', T1, T0, T1);
fprintf('Aç? aral???: [%.1f°, %.1f°]\n', min(Theta_f(:)), max(Theta_f(:)));

%% ?? Eleman No ? Fiber Aç?s? Tablosu ?????????????????????????????????????
% Eleman numaraland?rma: sol-alt = 1, sa?-üst = 100
% Görüntüdeki gibi: sat?r bazl?, soldan sa?a, alttan üste

elem_fiber = zeros(nex*ney, 2);  % [eleman_no, fiber_aç?s?]

for row = 1:ney
    for col = 1:nex
        eid = (row-1)*nex + col;
        elem_fiber(eid, 1) = eid;
        elem_fiber(eid, 2) = Theta_e(row, col);
    end
end

fprintf('\nEleman No  |  Fiber Aç?s? [°]\n');
fprintf('-----------|------------------\n');
fprintf('  %3d      |    %7.3f\n', elem_fiber');